const char *
_lookup_oui_(struct oui *table, uint32 oui)
{
	int i;
	for (i=0;table[i].name;i++) {
		if (oui == table[i].oui)
			return table[i].name;
		if (oui < table[i].oui)
			break;
	}

	return NULL;
}

const char *
lookup_oui(uint32 oui)
{
	const char *s;

	s = _lookup_oui_(quickOUI, oui);
	if (!s)
		s = _lookup_oui_(OUI, oui);
	return s ? s : "Unknown";
}
