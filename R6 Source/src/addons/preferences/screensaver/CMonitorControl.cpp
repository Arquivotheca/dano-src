CMonitorControl::CMonitorControl(BRect frame, const char *name, BMessage *message, long value)
 : BView(frame, name, B_FOLLOW_LEFT | B_FOLLOW_TOP, B_WILL_DRAW), other(0)
{
	SetMessage(message);
	mValue = value;
	mEnabled = true;
	mBitmap = new BBitmap(BRect(0, 0, 40, 34), B_CMAP8);
	
	SetValue(mValue);
}


CMonitorControl::~CMonitorControl()
{
	delete mBitmap;
}


void CMonitorControl::AttachedToWindow()
{
	BView *parent = Parent();
	if (parent != NULL)
		SetViewColor(parent->ViewColor());
	SetDrawingMode((mEnabled) ? B_OP_OVER : B_OP_BLEND);
}


void CMonitorControl::Draw(BRect r)
{
	DrawBitmap(mBitmap, r, r);
}


void CMonitorControl::MouseDown(BPoint where)
{
	if (!mEnabled)	
		return;
		
	long value = -1;
	
	BRect bounds = Bounds();
	float thirdW = bounds.Width() / 3;
	float thirdH = bounds.Height() / 3;
	
	BRect noRect;
	noRect.left = thirdW;
	noRect.top = thirdH;
	noRect.right = noRect.left + thirdW;
	noRect.bottom = noRect.top + thirdH;
	if (noRect.Contains(where))
		value = -1;
	else {
		float halfW = bounds.Width() / 2;
		float halfH = bounds.Height() / 2;
		
		if (where.x <= halfW) {
			if (where.y <= halfH)
				value = 0;
			else
				value = 3;
		}
		else {
			if (where.y <= halfH)
				value = 1;
			else
				value = 2;
		}
	}
	
	SetValue(value);
}

void CMonitorControl::SetValue(long inValue)
{
	char *image = NULL;
	
	switch(inValue)
	{
		case -1:
			image = (char *)kMonitorNo;
			break;
			
		case 0:
			image = (char *)kMonitorTL;
			break;
			
		case 1:
			image = (char *)kMonitorTR;
			break;
			
		case 3:
			image = (char *)kMonitorBL;
			break;
			
		case 2:
			image = (char *)kMonitorBR;
			break;
			
		default:
			image = NULL;
			break;
	}

	if(image != NULL)
	{
		mBitmap->SetBits(image, mBitmap->BitsLength(), 0, B_COLOR_8_BIT);
		Draw(Bounds());

		if(inValue != mValue)
		{
			mValue = inValue;
			BMessage copy(*Message());
			copy.AddInt8("value", mValue);
			Invoke(&copy);
		}
	}

	if(other && inValue >= 0 && inValue <= 3 && other->Value() == inValue)
		other->SetValue(-1);
}

void CMonitorControl::SetOther(CMonitorControl *o)
{
	other = o;
}

void CMonitorControl::SetEnabled(bool on)
{
	if(on != mEnabled)
	{
		mEnabled = on;

		SetDrawingMode((mEnabled) ? B_OP_OVER : B_OP_BLEND);
		Draw(Bounds());
	}
}
